package com.qingfeng.cms.domain.sign.enums;

import com.fasterxml.jackson.annotation.JsonFormat;
import com.qingfeng.currency.base.BaseEnum;
import io.swagger.annotations.ApiModel;
import io.swagger.annotations.ApiModelProperty;
import lombok.AllArgsConstructor;
import lombok.Getter;
import lombok.NoArgsConstructor;

/**
 * @author 清风学Java
 * @version 1.0.0
 * @date 2023/1/31
 */
@Getter
@AllArgsConstructor
@NoArgsConstructor
@ApiModel(value = "IsReleaseEnum", description = "签到状态-枚举")
@JsonFormat(shape = JsonFormat.Shape.OBJECT)
public enum SignStatusEnum implements BaseEnum {

    INIT("待签到"),
    FINISH("已签到"),
    ABANDONMENT("超时废弃");

    @ApiModelProperty(value = "描述")
    private String desc;

    public static SignStatusEnum match(String val, SignStatusEnum def) {
        for (SignStatusEnum enm : SignStatusEnum.values()) {
            if (enm.name().equalsIgnoreCase(val) || enm.desc.equals(val)) {
                return enm;
            }
        }
        return def;
    }

    public static SignStatusEnum get(String val) {
        return match(val, null);
    }

    public boolean eq(String val) {
        return this.name().equalsIgnoreCase(val);
    }

    public boolean eq(SignStatusEnum val) {
        if (val == null) {
            return false;
        }
        return eq(val.name());
    }

    @Override
    @ApiModelProperty(value = "编码")
    public String getDesc() {
        return this.desc;
    }
}
